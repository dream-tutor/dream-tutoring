// 동 → 시군구 역방향 맵 (빠른 조회용)
export const DONG_TO_GU = {};
export const DONG_TO_SIDO = {};
for (const [sido, gus] of Object.entries(VISIT_REGIONS)) {
  for (const [gu, dongs] of Object.entries(gus)) {
    for (const dong of dongs) {
      DONG_TO_GU[dong] = gu;
      DONG_TO_SIDO[dong] = sido;
    }
  }
}

// "동" 접미사 제거 함수
export function stripSuffix(name) {
  return name.replace(/(동|면|읍|리)$/, '');
}

// 시군구 "시/구/군" 접미사 제거
export function stripGuSuffix(name) {
  return name.replace(/(시|구|군)$/, '');
}

